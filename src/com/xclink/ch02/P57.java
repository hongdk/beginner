package com.xclink.ch02;

import java.util.Scanner;

public class P57 {
	
	public static void main(String[] args) {
		
		Scanner scanner=new Scanner(System.in);
		
		while(true){
			
			int month = scanner.nextInt();
			switch(month){
				
				case 3:
				case 4:
				case 5:
					System.out.println("春季");break;
				case 6:
				case 7:
				case 8:
					System.out.println("夏季");break;
				case 9:
				case 10:
				case 11:
					System.out.println("秋");break;
				case 12:
				case 1:
				case 2:
					System.out.println("冬");break;
				default:
					System.out.println("请输入正确的月份【1-12】");
					break;
				
			
			}
		}
		
		    
		 
	}
}

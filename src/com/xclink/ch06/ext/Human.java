public class Human {	
	
	private String head;
	private String legs;
	private String arms;
	
	public  void run(){
		System.out.println("人类在行走");
	}

	public String getHead() {
		return head;
	}

	public void setHead(String head) {
		this.head = head;
	}

	public String getLegs() {
		return legs;
	}

	public void setLegs(String legs) {
		this.legs = legs;
	}

	public String getArms() {
		return arms;
	}

	public void setArms(String arms) {
		this.arms = arms;
	}
	
	
	

}
